package StartJava;

public class DataTypeRanges {
    public static void main(String[] args) {
    byte ByteMin=  Byte.MIN_VALUE;
    byte ByteMax= Byte.MAX_VALUE;

    int IntMin= Integer.MIN_VALUE;
    int IntMax= Integer.MAX_VALUE;

    long LongMin= Long.MIN_VALUE;
    long LongMax= Long.MAX_VALUE;

    System.out.println ("The Byte Value ranges From " +ByteMin+ " To " +ByteMax);
    System.out.println ("The Integer Value ranges From " +IntMin+ " To " +IntMax);
    System.out.println ("The Long Value ranges From " +LongMin+ " To " +LongMax);
}
// 64397634973649 long;
    // long 56; byte
}
